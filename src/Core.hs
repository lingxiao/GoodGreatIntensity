module Core where


{-----------------------------------------------------------------------------
    I. System Messages
------------------------------------------------------------------------------}

data Message a   = Success
                 | FE (FileError a)
                 | Other a
                 deriving (Show)

data FileError a = FileDoesNotExist   a
                 | NonExistantInputDir a
                 | NonExistantOutDir   a
    deriving (Show)

